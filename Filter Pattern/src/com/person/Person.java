package com.person;

public class Person {
	
	String name;
	String gender;
	String maritalSatus;
	public Person(String name, String gender, String maritalSatus) {
		super();
		this.name = name;
		this.gender = gender;
		this.maritalSatus = maritalSatus;
	}
	public String getName() {
		return name;
	}
	public String getGender() {
		return gender;
	}
	public String getMaritalSatus() {
		return maritalSatus;
	}
	
	

}
